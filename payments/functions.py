
# this file contains the filter methods in python

# greater than
gt = lambda x, y: True if x > y else False

# greater than equal to
ge = lambda x, y: True if x >= y else False

# less than
lt = lambda x, y: True if x < y else False

# less than equal to
le = lambda x, y: True if x <= y else False

# equal to
eq = lambda x, y: True if x == y else False

# not equal to 
ne = lambda x, y: True if x != y else False
